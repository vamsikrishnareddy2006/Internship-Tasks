# ---------------------------------------------------------------
# 3. Integration Flow Diagram
# ---------------------------------------------------------------
img3 = Image.new("RGB", (W, H), SURFACE)
d3 = ImageDraw.Draw(img3)
d3.text((W / 2, 60), "DevPulse - Model Integration Flow", font=font(24, True), fill=TEXT, anchor="mm")

boxes = [
    ("HTML Templates", "index.html / result.html\n(User enters metrics)"),
    ("Flask Backend", "app.py\n/predict route receives form data"),
    ("ML Model (.pkl)", "best_model.pkl + scaler.pkl\nRandom Forest classifier"),
    ("Prediction Output", "Risk: Low / Medium / High\n+ confidence % rendered on page"),
]
bw, bh = 900, 130
bx = (W - bw) / 2
by = 130
colors = [PRIMARY, (99, 102, 241), (16, 185, 129), (245, 158, 11)]
for i, (title, sub) in enumerate(boxes):
    box = [bx, by, bx + bw, by + bh]
    rounded(d3, box, 14, fill=BG, outline=colors[i], width=3)
    d3.text((bx + 30, by + 20), title, font=font(18, True), fill=colors[i])
    lines = sub.split("\n")
    for j, line in enumerate(lines):
        d3.text((bx + 30, by + 55 + j * 22), line, font=font(13), fill=MUTED)
    if i < len(boxes) - 1:
        ay = by + bh
        d3.line([W / 2, ay, W / 2, ay + 40], fill=MUTED, width=2)
        d3.polygon([(W / 2 - 8, ay + 32), (W / 2 + 8, ay + 32), (W / 2, ay + 45)], fill=MUTED)
    by += bh + 55

img3.save(f"{OUT}/3_integration_flow.png")

print("Saved screenshots:", os.listdir(OUT))
