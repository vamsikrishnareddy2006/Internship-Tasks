from PIL import Image, ImageDraw, ImageFont
import os

OUT = "screenshots"
os.makedirs(OUT, exist_ok=True)

W, H = 1280, 900
PRIMARY = (108, 99, 255)
BG = (244, 245, 250)
SURFACE = (255, 255, 255)
TEXT = (34, 34, 59)
MUTED = (107, 114, 128)
BORDER = (229, 231, 235)
GREEN = (34, 197, 94)
RED = (239, 68, 68)
AMBER = (245, 158, 11)


def font(size, bold=False):
    paths = [
        "/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf" if bold else
        "/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf",
    ]
    for p in paths:
        if os.path.exists(p):
            return ImageFont.truetype(p, size)
    return ImageFont.load_default()


def rounded(draw, box, radius, fill=None, outline=None, width=1):
    draw.rounded_rectangle(box, radius=radius, fill=fill, outline=outline, width=width)


def navbar(draw, title_tag):
    draw.rectangle([0, 0, W, 70], fill=SURFACE)
    draw.line([0, 70, W, 70], fill=BORDER, width=1)
    draw.text((40, 22), "📊 DevPulse", font=font(22, True), fill=PRIMARY)
    rounded(draw, [W - 320, 20, W - 130, 50], 15, outline=BORDER, width=1)
    draw.text((W - 305, 27), title_tag, font=font(13), fill=MUTED)
    draw.ellipse([W - 100, 17, W - 64, 53], outline=BORDER, width=1)
    draw.text((W - 88, 26), "🌙", font=font(14))


def footer(draw):
    draw.text((W / 2 - 260, H - 40), "DevPulse · Developer Productivity & Code Quality Dashboard · Day 14 Internship Task",
               font=font(12), fill=MUTED)


# ---------------------------------------------------------------
# 1. Input Form Page
# ---------------------------------------------------------------
img = Image.new("RGB", (W, H), BG)
d = ImageDraw.Draw(img)
navbar(d, "Commit Risk Predictor")

d.text((W / 2, 110), "Commit Risk Prediction", font=font(30, True), fill=TEXT, anchor="mm")
d.text((W / 2, 145), "Enter a developer's weekly activity metrics to predict commit risk using the trained ML model.",
       font=font(14), fill=MUTED, anchor="mm")

# flow strip
steps = ["HTML Form", "→", "Flask Backend", "→", "ML Model (.pkl)", "→", "Prediction"]
x = 210
for s in steps:
    if s == "→":
        d.text((x, 182), s, font=font(16), fill=MUTED)
        x += 30
    else:
        tw = d.textlength(s, font=font(13, True))
        rounded(d, [x, 172, x + tw + 30, 202], 15, fill=SURFACE, outline=BORDER)
        d.text((x + 15, 180), s, font=font(13, True), fill=PRIMARY)
        x += tw + 50

# form card
card = [60, 230, W - 60, 740]
rounded(d, card, 14, fill=SURFACE, outline=BORDER)

fields = [
    ("Commits per Week", "8"), ("Lines Added", "89"), ("Lines Deleted", "54"),
    ("Files Changed", "1"), ("Bugs Reported", "3"), ("Code Review Comments", "0"),
    ("Avg Review Time (hrs)", "4.5"), ("Test Coverage (%)", "67.0"),
    ("Deployment Frequency", "2"), ("Late-Night Commits", "1"),
]
cols = 3
fx0, fy0 = 100, 270
cell_w, cell_h = (card[2] - card[0] - 80) / cols, 105
for i, (label, val) in enumerate(fields):
    col, row = i % cols, i // cols
    fx = fx0 + col * cell_w
    fy = fy0 + row * cell_h
    d.text((fx, fy), label, font=font(13, True), fill=MUTED)
    rounded(d, [fx, fy + 22, fx + cell_w - 30, fy + 58], 8, fill=BG, outline=BORDER)
    d.text((fx + 12, fy + 32), val, font=font(14), fill=TEXT)

rounded(d, [card[2] - 340, 660, card[2] - 190, 705], 8, outline=PRIMARY, width=2)
d.text((card[2] - 320, 673), "Fill Sample Data", font=font(13, True), fill=PRIMARY)
rounded(d, [card[2] - 180, 660, card[2] - 40, 705], 8, fill=PRIMARY)
d.text((card[2] - 160, 673), "Predict Risk", font=font(13, True), fill=(255, 255, 255))

footer(d)
img.save(f"{OUT}/1_input_form.png")

